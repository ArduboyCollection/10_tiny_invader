#include "rnd.h"
#include "tinymt32.h"


//---------------------------------------------------------------------------
tinymt32_t RndTinyMt;


//---------------------------------------------------------------------------
void RndInit(void)
{
	tinymt32_init(&RndTinyMt, 0xF67C129B);
}
//---------------------------------------------------------------------------
void RndInitSeed(u32 num)
{
	tinymt32_init(&RndTinyMt, num);
}
//---------------------------------------------------------------------------
u32 Rnd32(void)
{
	return tinymt32_generate_uint32(&RndTinyMt);
}
//---------------------------------------------------------------------------
u32 Rnd(u32 num)
{
	s32 ret = Rnd32() % num;

	if(ret < 0)
	{
		ret *= -1;
	}

	return ret;
}
//---------------------------------------------------------------------------
bool RndIsBool(void)
{
	return (Rnd32() & 0x1) ? TRUE : FALSE;
}
